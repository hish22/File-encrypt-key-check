void save_key(char *file_key_name) {
    FILE *fk = fopen(file_key_name,"wb");
    if (fk != NULL) {
        fwrite(&key, sizeof(key), 1, fk);
        fclose(fk);
    }
}

/* Generate key */
void generate_key(char *filename) {
    // Phase Two: read file bytes and status
    struct stat file_info;
    if (stat(filename, &file_info) == 0 ) {
        void *file_data = malloc(file_info.st_size);
        unsigned char* file_data_as_byte = (unsigned char*) file_data;
        FILE *fh = fopen(filename,"rb");
        if (fh != NULL) {
            fread(file_data,file_info.st_size,1,fh);
            fclose(fh);
        }

        int i = 0;
        int j = 0;
        int count = 0;
        while (count < file_info.st_size) {
            key[i][j] = *file_data_as_byte ^ key[i][j];
            file_data_as_byte++;
            j++;
            if(i == 32) {
                i = 0;
                j = 0;
            } else if (j == 32) {
                i++;
                j = 0;
            }
            count++;
        }

        free(file_data);
    }

}

void bring_key(char *file_key_name,unsigned char buf[32][32]) {  
    FILE *fk = fopen(file_key_name,"rb");
    if(fk != NULL) {
        fread(buf, 32 * 32, 1, fk);
        fclose(fk);
    }
}

/* Check file based on key */
int check_file(char *filename, char *file_key, unsigned char buffer_key[32][32]) {
    generate_key(filename);
    bring_key(file_key, buffer_key);

    for(int i = 0; i < 32; i++) {
        for(int j = 0; j < 32; j++) {
            if(key[i][j] != buffer_key[i][j]) {
                return FALSE;
            }
        }
    }
    return TRUE;
}

void print_pixel(pixel_t* pixel) {
    char bg = '4';
    char fg = pixel->color + '0';
    
    char symbol;
    switch (pixel->symbol) {
        case 0: symbol = '-'; break;
        case 1: symbol = '.'; break;
        case 2: symbol = '#'; break;
        case 3: symbol = '_'; break;
        case 4: symbol = '@'; break;
        case 5: symbol = '$'; break;
        case 6: symbol = '^'; break;
        case 7: symbol = '*'; break;
        default: symbol = '?'; break;
    }

    printf("\033[0;1;%c%cm%c\033[0m ", bg, fg, symbol);
}

void draw_side_by_side(unsigned char key[32][32], unsigned char buf[32][32]) {
    for (int i = 0; i < 32; i++) {

        for (int j = 0; j < 32; j++) {
            print_pixel((pixel_t*)&key[i][j]);
        }

        printf("    "); 

        for (int j = 0; j < 32; j++) {
            print_pixel((pixel_t*)&buf[i][j]);
        }

        printf("\n");
    }
}

int main(int argv, char **argc) {
    // Phase one: accept command-line from user

    if(argv != 4) {
        printf("Wrong command usage, please do: %s (-g/-c) filename key\n",argc[0]);
        exit(1);
    }

    char *flag = argc[1];
    char *filename = argc[2];
    char *file_key = argc[3];

    if (flag[0] == '-') {
        switch (flag[1]) {
        case 'g':
            generate_key(filename);
            save_key(file_key);
            break;
        
        case 'c':
            unsigned char buffer_key[32][32];
            if (check_file(filename, file_key, buffer_key)) {
                printf("[%sSUCCESS%s] Keys match: The file signature is valid.\n", GREEN, RESET);
            } else {
                printf("[%sERROR%s]   Keys mismatch: The file signature is invalid.\n", RED, RESET);
            }
            draw_side_by_side(key, buffer_key);
            break;
        
        default:
            printf("Wrong flag used, please use -g or -c\n");
            break;
        }
    }

    return 0;
}